#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	int		start;
	int		end;
	char	*trim_s;

	if (s1 == NULL || set == NULL)
		return (NULL);
	start = 0;
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	end = ft_strlen(s1);
	while (end > start && ft_strchr(set, s1[end - 1]))
		end--;
	trim_s = (char *)malloc(sizeof(char) * (end - start + 1));
	if (!trim_s)
		return (NULL);
	ft_strlcpy(trim_s, &s1[start], end - start + 1);
	return (trim_s);
}
